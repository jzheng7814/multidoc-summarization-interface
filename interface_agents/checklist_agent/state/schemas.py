"""Pydantic schemas for the legal agent system."""

from typing import Any, Dict, List, Literal, Optional, Tuple, Union
from datetime import datetime

import pytz
from pydantic import BaseModel, Field, validator


class Evidence(BaseModel):
    """Sentence-span evidence supporting an extracted value."""

    source_document_id: str = Field(
        ...,
        description="Stable document ID from list_documents",
    )
    start_sentence: int = Field(
        ...,
        ge=1,
        description="Inclusive starting sentence ID (1-indexed)",
    )
    end_sentence: int = Field(
        ...,
        ge=1,
        description="Inclusive ending sentence ID (1-indexed)",
    )

    @validator("end_sentence")
    def validate_sentence_range(cls, v, values):
        start_sentence = values.get("start_sentence")
        if start_sentence is not None and v < start_sentence:
            raise ValueError("end_sentence must be >= start_sentence")
        return v


class ExtractedItem(BaseModel):
    """A single extracted value with its supporting evidence."""
    evidence: List[Evidence]
    value: str = Field(..., description="The extracted information from the evidence")
    
    @validator('evidence')
    def validate_has_evidence(cls, v):
        if not v:
            raise ValueError('ExtractedItem must have at least one evidence')
        return v


class ChecklistItem(BaseModel):
    """A single checklist item with its extracted values."""
    key: str
    extracted: List[ExtractedItem] = []
    last_updated: datetime = Field(default_factory=lambda: datetime.now(pytz.timezone('America/New_York')))


class DocumentCoverage(BaseModel):
    """Tracks sentence coverage of a document."""

    windows_read: int = 0
    approx_sentences_read: int = 0
    sentence_ranges: List[Tuple[int, int]] = Field(
        default_factory=list,
        description="List of (start, end) inclusive sentence ranges viewed",
    )


class DocumentReadInfo(BaseModel):
    """Last read position in a document."""

    start_sentence: int
    end_sentence: int


class DocumentInfo(BaseModel):
    """Information about a document in the corpus."""

    doc_id: str
    type: str  # Flexible string type instead of enum
    sentence_count: int
    visited: bool = False
    coverage: DocumentCoverage = Field(default_factory=DocumentCoverage)
    last_read: Optional[DocumentReadInfo] = None


class ActionRecord(BaseModel):
    """Record of an action taken by the orchestrator."""
    tool: str
    target: Dict[str, Any]
    step: int  # The actual step number when this action was taken
    purpose: Optional[str] = None
    changed_keys: List[str] = []
    timestamp: datetime = Field(default_factory=lambda: datetime.now(pytz.timezone('America/New_York')))
    success: bool = True  # Whether the action succeeded
    error: Optional[str] = None  # Error message if action failed
    validation_errors: List[str] = []  # Business logic errors from tool output
    result_summary: Optional[Dict[str, Any]] = None  # Store tool result for formatting
    auto_generated: bool = False  # Whether this action was auto-generated by the system


class RunHeader(BaseModel):
    """Metadata for a run."""
    run_id: str
    step: int
    timestamp: datetime = Field(default_factory=lambda: datetime.now(pytz.timezone('America/New_York')))


class Task(BaseModel):
    """Task specification."""
    user_instruction: str = Field(..., description="What the user wants the agent to do")
    constraints: List[str] = Field(default_factory=list, description="Optional task-specific constraints beyond system prompt")
    checklist_definitions: Dict[str, str] = Field(default_factory=dict, description="Definitions of checklist items to extract")
    focus_context: Optional[str] = Field(
        default=None,
        description="Optional focus context describing the target matter and exclusion boundaries",
    )


class DerivedStateEntry(BaseModel):
    """One persisted derived-state entry."""

    id: str
    text: str
    source_document_ids: List[str] = Field(
        default_factory=list,
        description="Optional supporting document IDs that ground this entry",
    )
    pinned: bool = Field(
        default=True,
        description="Pinned entries are injected into the prompt; unpinned entries are hidden context",
    )
    last_updated: datetime = Field(default_factory=lambda: datetime.now(pytz.timezone('America/New_York')))


class DerivedState(BaseModel):
    """Persistent derived state memory board."""

    confirmed_state: List[DerivedStateEntry] = Field(default_factory=list)
    open_questions: List[DerivedStateEntry] = Field(default_factory=list)
    external_refs: List[DerivedStateEntry] = Field(default_factory=list)


