# Tool I/O schemas

class ListDocumentsOutput(BaseModel):
    """Output of list_documents tool."""
    documents: List[DocumentInfo]


class ReadDocumentInput(BaseModel):
    """Input for read_document tool."""

    doc_id: str
    start_sentence: int = Field(..., ge=1)
    end_sentence: int = Field(..., ge=1)

    @validator("end_sentence")
    def validate_sentence_range(cls, v, values):
        start_sentence = values.get("start_sentence")
        if start_sentence is not None and v < start_sentence:
            raise ValueError("end_sentence must be >= start_sentence")
        return v


class ReadDocumentOutput(BaseModel):
    """Output of read_document tool."""

    doc_id: str
    start_sentence: int
    end_sentence: int
    text: str


class RegexMatch(BaseModel):
    """A single regex match result."""

    start_sentence: int
    end_sentence: int
    start_char: int
    end_char: int
    snippet: str
    groups: Dict[str, str] = {}
    pattern: str
    flags: List[str] = []


class SearchDocumentRegexInput(BaseModel):
    """Input for search_document_regex tool."""

    doc_id: Optional[str] = None  # Single document ID or "all"
    doc_ids: Optional[List[str]] = None  # Array of specific document IDs
    pattern: str
    flags: List[Literal["IGNORECASE", "MULTILINE", "DOTALL"]] = []
    top_k: int = Field(5, ge=1, le=20)

    @validator("doc_id", "doc_ids")
    def validate_at_least_one_doc_specified(cls, v, values):
        """Ensure at least one of doc_id/doc_ids is provided."""
        if "doc_id" not in values and "doc_ids" not in values and v is None:
            raise ValueError("Either doc_id or doc_ids must be provided")
        return v


class DocumentSearchResult(BaseModel):
    """Search results for a single document."""

    doc_id: str
    matches: List[RegexMatch]
    match_count: int


class SearchDocumentRegexOutput(BaseModel):
    """Output of search_document_regex tool."""

    pattern: str
    documents_searched: List[str]  # List of document IDs that were searched
    results: List[DocumentSearchResult]  # Per-document results
    total_matches: int  # Total matches across all documents


class GetChecklistOutput(BaseModel):
    """Output of get_checklist tool."""
    checklist: List[ChecklistItem]
    completion_stats: Dict[str, int] = Field(
        default_factory=lambda: {
            "filled": 0,
            "empty": 0,
            "total": 0  # Will be dynamically set based on checklist configuration
        }
    )


class ChecklistPatch(BaseModel):
    """A patch to update a checklist item."""
    key: str
    extracted: Optional[List[ExtractedItem]] = None
    # Support incremental additions
    add_extracted: Optional[List[ExtractedItem]] = None
    
    @validator('extracted')
    def validate_extracted_has_evidence(cls, v):
        if v is not None:
            for item in v:
                if not item.evidence:
                    raise ValueError('Each extracted item must have at least one evidence')
        return v
    
    @validator('add_extracted')
    def validate_add_extracted_has_evidence(cls, v):
        if v is not None:
            for item in v:
                if not item.evidence:
                    raise ValueError('Each extracted item must have at least one evidence')
        return v


class UpdateChecklistInput(BaseModel):
    """Input for update_checklist tool."""
    patch: List[ChecklistPatch]


class UpdateChecklistOutput(BaseModel):
    """Output of update_checklist tool."""
    updated_keys: List[str]
    validation_errors: List[str] = []
    success: bool


class AppendChecklistOutput(BaseModel):
    """Output of append_checklist tool."""
    appended_keys: List[str]
    items_added: Dict[str, int] = Field(default_factory=dict)
    duplicates_skipped: Dict[str, int] = Field(default_factory=dict)
    validation_errors: List[str] = []
    success: bool


class StopDecision(BaseModel):
    """Stop decision from orchestrator."""
    decision: Literal["stop"]
    reason: str
    remaining_empty_keys: List[str] = []


class OrchestratorAction(BaseModel):
    """Action chosen by orchestrator."""
    tool: Optional[str] = None
    args: Optional[Dict[str, Any]] = None
    decision: Optional[Literal["stop"]] = None
    reason: Optional[str] = None
    remaining_empty_keys: Optional[List[str]] = None
    stats: Optional[Dict[str, Any]] = None  # Token usage statistics
    parse_failed: Optional[bool] = False  # Flag for parse/validation errors
    
    @validator('tool')
    def validate_action_type(cls, v, values):
        # Allow special error actions
        if v in ['parse_error', 'validation_error']:
            return v
        if v is None and values.get('decision') != 'stop':
            raise ValueError('Must specify either tool or decision=stop')
        if v is not None and values.get('decision') == 'stop':
            raise ValueError('Cannot specify both tool and stop decision')
        return v


# Ledger schemas for tracking reads and searches

class ReadEvent(BaseModel):
    """Record of a document read."""

    timestamp: datetime = Field(default_factory=lambda: datetime.now(pytz.timezone('America/New_York')))
    doc_id: str
    start_sentence: int
    end_sentence: int
    sentences_read: int
    step: int


class SearchEvent(BaseModel):
    """Record of a regex search (single or multi-document)."""

    timestamp: datetime = Field(default_factory=lambda: datetime.now(pytz.timezone('America/New_York')))
    # Search specification - exactly one of these will be set.
    doc_id: Optional[str] = None  # Single doc ID or "all"
    doc_ids: Optional[List[str]] = None  # List of specific doc IDs
    pattern: str
    flags: List[str]
    matches_found: int  # Total matches across all docs
    step: int
    # Per-document details (includes docs with 0 matches)
    document_matches: Dict[str, List[Tuple[int, int]]] = Field(
        default_factory=dict,
        description="Map of doc_id to list of (start_sentence, end_sentence) ranges",
    )


class UpdateEvent(BaseModel):
    """Record of a checklist update."""
    timestamp: datetime = Field(default_factory=lambda: datetime.now(pytz.timezone('America/New_York')))
    keys_updated: List[str]
    patch: List[ChecklistPatch]
    step: int
    success: bool


class ToolEvent(BaseModel):
    """Generic tool execution event."""
    timestamp: datetime = Field(default_factory=lambda: datetime.now(pytz.timezone('America/New_York')))
    tool_name: str
    args: Dict[str, Any]
    result: Optional[Dict[str, Any]] = None
    success: bool = True
    step: int


class LedgerEntry(BaseModel):
    """Generic ledger entry."""
    event_name: str  # Changed from event_type to event_name, will be the actual tool name
    event: Union[ReadEvent, SearchEvent, UpdateEvent, ToolEvent]
    run_id: str
    step: int
